#include "libft.h"

int	ft_atoi(const char *str)
{
	int				i;
	int				signe;
	unsigned long	res;

	i = 0;
	signe = 1;
	res = 0;
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == 32)
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			signe = -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		res = res * 10 + str[i] - '0';
		i++;
	}
	if (res > 9223372036854775807 && signe == -1)
		return (0);
	else if (res > 9223372036854775807 && signe == 1)
		return (-1);
	return (signe * res);
}
